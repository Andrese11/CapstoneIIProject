        <script>document.getElementById("sortItems").addEventListener("change", function() {
            const selectedValue = this.value;
            const products = document.querySelectorAll(".product-item");
        
            products.forEach(product => {
                // Show all products if "All" is selected
                if (selectedValue === "All") {
                    product.style.display = "block";
                } else {
                    const gender = product.getAttribute("data-gender");
                    const type = product.getAttribute("data-type");
        
                    // Check if the product matches the selected filter
                    if (selectedValue === gender || selectedValue === type) {
                        product.style.display = "block";
                    } else {
                        product.style.display = "none";
                    }
                }
            });
        });
